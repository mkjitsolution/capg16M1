package p1;

public class SwitchCaseDemo {

	public static void main(String[] args) {
		
		int token = 30;

		switch(token)
		{
		case 1:
			System.out.println("1");
			break;
		case 30:
			System.out.println("30");
			return;
		case 10:
			System.out.println("10");
			break;
			
		}
		
	}
}
